export const splayPics = () => {
    const meatDump = document.getElementById("meat");
    const picFrag = document.createDocumentFragment();

    aarinComplex.map(e => {
        const panel = document.createElement("article");
        panel.classList.add("inv-panel");

        const invImg = document.createElement("img");
        invImg.src = `https://forestw70.github.io/arw${e.coverUrl}`;
        invImg.width = e.coverXY[0] / 4;
        invImg.height = e.coverXY[1] / 4;

        const imgTit = document.createElement("p");
        imgTit.innerText = `"${getTitle(e.coverUrl)}"`;

        const loadWorkModal = () => {
            const wImg = document.getElementById("shownWork");
            const wTit = document.getElementById("workTitle");
            const wUrl = document.getElementById("workUrl");
            const wDate = document.getElementById("workDate");
            const wBlurb = document.getElementById("workBlurb");
        
            wImg.src = "";
            wTit.innerText = "";
            wUrl.innerText = "";

            wImg.src = `https://forestw70.github.io/arw${e.coverUrl}`;
            wTit.innerText = getTitle(e.coverUrl);
            wUrl.innerText = e.coverUrl;
        
            if (e.hit <= 1700) {
                wImg.width = e.coverXY[0] / 2;
                wImg.height = e.coverXY[1] / 2;
            } else {
                wImg.width = e.coverXY[0] / 3;
                wImg.height = e.coverXY[1] / 3;
        
            }

            const thumbCont = document.getElementById("tinyBoys");
            const thumbThru = document.createDocumentFragment();
            e.works.map(ee => {
                const thumb = document.createElement("img");
                thumb.src = `https://forestw70.github.io/arw${ee.url}`;
                thumb.dataset.dim = ee.xy[0] + " " + ee.xy[1];
                thumb.width = ee.xy[0] / 10;
                thumb.height = ee.xy[1] / 10;

                const thumb2Hero = (el) => {
                    wImg.src = el.target.src;
                    wImg.width = el.target.dataset.dim.split(" ")[0] / 3;
                    wImg.height = el.target.dataset.dim.split(" ")[1] / 3;
                }

                thumb.addEventListener("click", thumb2Hero)
                thumbThru.appendChild(thumb);
            })
            thumbCont.innerText = "";
            thumbCont.appendChild(thumbThru);
        
            const worksPanel = document.getElementById("workModal");
            const backdrop = document.getElementById("modalLand");
            const optionsMenu = document.getElementById("menuModal");
            worksPanel.classList.remove("hide");
            optionsMenu.classList.add("hide");
            backdrop.classList.toggle("active");
        }

        panel.addEventListener("click", loadWorkModal);

        panel.appendChild(invImg);
        panel.appendChild(imgTit);

        if (Math.random() >= .5) {
            picFrag.appendChild(panel);

        } else {
            picFrag.insertBefore(panel, picFrag.firstChild);
        }
    });

    meatDump.appendChild(picFrag);
}